-- ─── Reporting views for QuickSight / Streamlit ───────────────────────────────
-- Fixed: removed FULL JOIN ... ON TRUE (cross-join that multiplied rows by millions).
-- Each metric is now an independent scalar subquery — zero risk of row-multiplication.
CREATE OR REPLACE VIEW CRYPTO_SURVEILLANCE.ANALYTICS.VW_KRIs AS
SELECT
    CURRENT_DATE() AS report_date,
    -- Alert volume (independent subqueries, no cross-join)
    (SELECT COUNT_IF(detected_at >= DATEADD('day', -1, CURRENT_TIMESTAMP()))
     FROM CRYPTO_SURVEILLANCE.ANALYTICS.ALERTS)                              AS alerts_24h,
    (SELECT COUNT_IF(detected_at >= DATEADD('day', -7, CURRENT_TIMESTAMP()))
     FROM CRYPTO_SURVEILLANCE.ANALYTICS.ALERTS)                              AS alerts_7d,
    (SELECT COUNT_IF(severity = 'CRITICAL'
                     AND detected_at >= DATEADD('day', -1, CURRENT_TIMESTAMP()))
     FROM CRYPTO_SURVEILLANCE.ANALYTICS.ALERTS)                              AS critical_alerts_24h,
    -- Case metrics
    (SELECT COUNT_IF(state = 'OPEN')         FROM CRYPTO_SURVEILLANCE.ANALYTICS.CASES) AS open_cases,
    (SELECT COUNT_IF(state = 'IN_REVIEW')    FROM CRYPTO_SURVEILLANCE.ANALYTICS.CASES) AS cases_in_review,
    (SELECT COUNT_IF(state = 'ESCALATED')    FROM CRYPTO_SURVEILLANCE.ANALYTICS.CASES) AS escalated_cases,
    (SELECT COUNT_IF(sla_breached = TRUE)    FROM CRYPTO_SURVEILLANCE.ANALYTICS.CASES) AS sla_breached_cases,
    -- Risk coverage
    (SELECT COUNT_IF(aml_risk_rating IN ('HIGH','CRITICAL'))
     FROM CRYPTO_SURVEILLANCE.HARMONISED.ENTITY)                             AS high_risk_entities,
    (SELECT COUNT_IF(sanctions_flag = TRUE)
     FROM CRYPTO_SURVEILLANCE.HARMONISED.ENTITY)                             AS sanctioned_entities;

CREATE OR REPLACE VIEW CRYPTO_SURVEILLANCE.ANALYTICS.VW_KPIs AS
SELECT
    CURRENT_DATE()                                          AS report_date,
    -- Detection quality
    ROUND(AVG(CASE WHEN resolution_type = 'FALSE_POSITIVE' THEN 1.0 ELSE 0.0 END), 4)
        AS false_positive_rate,
    ROUND(AVG(DATEDIFF('minute', created_at, closed_at)), 0)
        AS avg_case_closure_minutes,
    ROUND(AVG(CASE WHEN sla_breached THEN 0.0 ELSE 1.0 END), 4)
        AS sla_compliance_rate,
    -- Throughput
    COUNT_IF(closed_at >= DATEADD('day', -7, CURRENT_TIMESTAMP())) AS cases_closed_7d,
    COUNT_IF(created_at >= DATEADD('day', -7, CURRENT_TIMESTAMP())) AS cases_opened_7d
FROM CRYPTO_SURVEILLANCE.ANALYTICS.CASES;

